using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;

namespace GestaoServico.Models
{
    public class Pessoa : Entity
    {
        private const string emailRegex = @"^((([a-z]|\d|[!#\$%&'\*\+\-\/=\?\^_`{\|}~]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])+(\.([a-z]|\d|[!#\$%&'\*\+\-\/=\?\^_`{\|}~]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])+)*)|((\x22)((((\x20|\x09)*(\x0d\x0a))?(\x20|\x09)+)?(([\x01-\x08\x0b\x0c\x0e-\x1f\x7f]|\x21|[\x23-\x5b]|[\x5d-\x7e]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])|(\\([\x01-\x09\x0b\x0c\x0d-\x7f]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF]))))*(((\x20|\x09)*(\x0d\x0a))?(\x20|\x09)+)?(\x22)))@((([a-z]|\d|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])|(([a-z]|\d|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])([a-z]|\d|-|\.|_|~|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])*([a-z]|\d|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])))\.)+(([a-z]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])|(([a-z]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])([a-z]|\d|-|\.|_|~|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])*([a-z]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])))\.?$";

        [Required(ErrorMessage = "O nome da pessoa é obrigatório")]
        [MinLength(3, ErrorMessage = "Esse nome é muito curto para uma pessoa real")]
        [MaxLength(128, ErrorMessage = "Esse nome é muito grande para uma pessoa real")]
        public string Nome { get; set; }

        [Required(ErrorMessage = "Informe uma data de nascimento válida")]
        [Display(Name = "Data de nascimento")]
        [DisplayFormat(DataFormatString = "mm/dd/yyyy")]
        public DateTime Nascimento { get; set; }

        [Required(ErrorMessage = "Informe um e-mail válido")]
        [DataType(DataType.EmailAddress)]
        [RegularExpression(emailRegex, ErrorMessage = "Informe um e-mail válido")] 
        public string Email {get; set;}

        [Required(ErrorMessage = "Informe o número do CPF")]
        public string Cpf {get; set;}
        public List<Telefone> Contato { get; set;}

    }
}